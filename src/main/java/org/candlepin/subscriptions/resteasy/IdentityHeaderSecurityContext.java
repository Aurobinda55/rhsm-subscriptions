package org.candlepin.subscriptions.resteasy;

import com.fasterxml.jackson.databind.ObjectMapper;

import java.security.Principal;

import javax.ws.rs.core.SecurityContext;

/**
 * Security context that uses the x-rh-identity header.
 */
public class IdentityHeaderSecurityContext implements SecurityContext {

    private final IdentityHeaderPrincipal principal;

    public IdentityHeaderSecurityContext(ObjectMapper mapper, String header) {
        this.principal = new IdentityHeaderPrincipal(mapper, header);
    }

    @Override
    public Principal getUserPrincipal() {
        return principal;
    }

    @Override
    public boolean isUserInRole(String role) {
        return false;
    }

    @Override
    public boolean isSecure() {
        return false;
    }

    @Override
    public String getAuthenticationScheme() {
        return "3Scale";
    }
}
